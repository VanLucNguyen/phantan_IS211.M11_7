--Chi nhanh quan ly tuyen tau
CREATE TABLE CHI_NHANH_QL
(
	MA_CN CHAR(4) PRIMARY KEY,
	TEN_CN VARCHAR2(100),
	DIA_CHI VARCHAR2(100)
);

--Ga
CREATE TABLE GA
(
	MA_GA CHAR(4) PRIMARY KEY,
	TEN_GA VARCHAR2(100),
	VI_TRI VARCHAR2(100),
	TINH_TRANG_GA VARCHAR2(100)
);

--Tuyen tau quan ly
CREATE TABLE TUYEN_TAU_QL
(
	MA_TUYEN CHAR(4) PRIMARY KEY,
	MA_CN CHAR(4),
	TINH_TRANG_TUYEN VARCHAR2(100)
);

-- Tuyen tau nhan vien
CREATE TABLE TUYEN_TAU_NV
(
	MA_TUYEN CHAR(4) PRIMARY KEY,
	TEN_TUYEN VARCHAR2(100),
	MA_GA_XUAT_PHAT CHAR(4),
	MA_GA_KET_THUC CHAR(4),
	LOAI_TUYEN VARCHAR2(100),
	GIA_TUYEN NUMBER(19,4),
	THOI_GIAN_BAT_DAU VARCHAR2(100),
	THOI_GIAN_KET_THUC VARCHAR2(100),
	THOI_GIAN_CHO VARCHAR2(100)
);

--Tuyen toc hanh
CREATE TABLE TUYEN_TOC_HANH
(
	MA_TUYEN CHAR(4) PRIMARY KEY,
	DUNG_TRAM NUMBER(1)
);

--Diem dung
CREATE TABLE DIEM_DUNG
(
  	MA_GA CHAR(4) ,
  	MA_TUYEN CHAR(4),
  	THOI_GIAN_DUNG VARCHAR2(100),
  	CONSTRAINT PK_DIEM_DUNG PRIMARY KEY (MA_GA, MA_TUYEN)
);

--Ve
CREATE TABLE VE
(
  	MA_VE CHAR(4) PRIMARY KEY,
  	TRI_GIA NUMBER(19,4),
  	MA_TUYEN CHAR(4),
  	NGAY_MUA DATE,
  	MA_KH CHAR(4),
  	MA_NV CHAR(4),
  	LOAI_VE VARCHAR2(100)
);

--Ve thuong
CREATE TABLE VE_THUONG
(
  	MA_VE CHAR(4) PRIMARY KEY,
  	THOI_DIEM_DUNG_VE DATE,
  	TINH_TRANG_VE	VARCHAR2(100),
  	MA_KH CHAR(4)
);

--Ve thang
CREATE TABLE VE_THANG
(
  	MA_VE CHAR(4) PRIMARY KEY,
  	SO_LAN_SU_DUNG INT,
  	TINH_TRANG_VE VARCHAR2(100),
  	NGAY_BAT_DAU DATE,
  	NGAY_KET_THUC DATE,
  	MA_KH CHAR(4)
);

--Thoi diem su dung
CREATE TABLE THOI_DIEM_SU_DUNG
(
  	MA_TD  INT GENERATED ALWAYS AS IDENTITY(START WITH 1 INCREMENT BY 1) PRIMARY KEY,	
  	MA_VE CHAR(4),
  	NGAY DATE
);

--Nhan vien
CREATE TABLE NHAN_VIEN
(
  	MA_NV CHAR(4) PRIMARY KEY,
  	HO_TEN VARCHAR2(100),
  	DIA_CHI VARCHAR2(100),
  	SO_DT VARCHAR2(100),
  	LUONG NUMBER(19,4),
  	MA_CN CHAR(4)
);

--Khach hang
CREATE TABLE KHACH_HANG
(
  	MA_KH CHAR(4) PRIMARY KEY,
  	HO_TEN VARCHAR2(100),
  	SDT VARCHAR2(100),
  	CMND VARCHAR2(100),
  	LOAI_KH VARCHAR2(100)
);

