CREATE TABLE BaiTap2.USER_NEW
(
    U_ID NUMBER NOT NULL,
    USERNAME VARCHAR2(50),
    PASS VARCHAR2(20),
    REGDAY DATE,
    NATIONALITY VARCHAR2(30),
    CONSTRAINT PK_USER PRIMARY KEY(U_ID)    
);
CREATE TABLE BaiTap2.CHANNEL
(
    CHANNELID VARCHAR2(4) NOT NULL,
    CNAME VARCHAR2(20),
    SUBSCRIBES NUMBER,
    OWNNER NUMBER REFERENCES BaiTap2.USER_NEW(U_ID),
    CREATED DATE,
    CONSTRAINT PK_CHANNEL PRIMARY KEY(CHANNELID)
);
CREATE TABLE BaiTap2.VIDEO 
(
    VIDEOID VARCHAR2(7) NOT NULL,
    TITTLE VARCHAR2(50),
    DURATION NUMBER,
    AGE NUMBER,
    CONSTRAINT PK_VIDEO PRIMARY KEY(VIDEOID)
)
CREATE TABLE BaiTap2.SHARE_VIDEO
(
    VIDEOID VARCHAR2(7) REFERENCES BaiTap2.VIDEO(VIDEOID),
    CHANNELID VARCHAR2(4)REFERENCES BaiTap2.CHANNEL(CHANNELID),
    CONSTRAINT PK_SHARE PRIMARY KEY(VIDEOID)
)

